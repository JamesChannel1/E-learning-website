document.getElementById("button0").addEventListener("click", myFunction0);

function myFunction0() {
  var thing00 = document.getElementById("user_input0").value;
  var thing01 = "four"

  if(thing00 === thing01)
  {
    document.getElementById("button0").innerHTML = "correct! you win";
    this.style.backgroundColor = "green";
  } 
  else if(thing00 != thing01)
  {
    document.getElementById("button0").innerHTML = "Incorrect, try again";
    this.style.backgroundColor = "red";
  }
}



